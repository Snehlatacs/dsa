package BitManipulation;

public class Bitwise_Operators {
    public static void main(String[] args) {
        int x = 5, y = 6;

        System.out.println(x&y);
        System.out.println(x|y);
        System.out.println(x^y);
        System.out.println(x<<2);
        System.out.println(y>>1);
        System.out.println(~x);
    }
}
